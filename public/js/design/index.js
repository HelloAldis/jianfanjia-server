$(function(){
	//注册一个Handlebars模版，通过id找到某一个模版，获取模版的html框架
	var myTemplate = Handlebars.compile($("#design-template").html());
	//装修风格
	Handlebars.registerHelper("decorate",function(value){
	    if(value==0){
	    	return "中式";
	    }else if(value==1){
	    	return "欧式";
	    }else if(value==2){
	    	return "现代";
	    }else if(value==3){
	    	return "地中海";
	    }else if(value==4){
	    	return "美式";
	    }else if(value==5){
	    	return "东南亚";
	    }else if(value==6){
	    	return "港澳台";
	    }else if(value==7){
	    	return "古典";
	    }else if(value==8){
	    	return "后现代";
	    }else if(value==9){
	    	return "非主流";
	    }else if(value==10){
	    	return "简约";
	    }
	 });
		//装修接单价格
		Handlebars.registerHelper("commit",function(value){
	    if(value==0){
	    	return "50-100";
	    }else if(value==1){
	    	return "100-200";
	    }else if(value==2){
	    	return "200-300";
	    }else if(value==3){
	    	return "300以上";
	    }
	 });
		// 取模li隔行变色
		Handlebars.registerHelper("addOne",function(index){
	    return (index %= 2) ? 'even' : '';
	});
	var $design = $("#j-design-list")
	$design.html(myTemplate(data));
	var arr = [];
	$design.find('li').each(function(i,el){
		arr.push($(el))
		$(el).remove();
	})
	var page = new Pageing({
		id : 'j-page',
		allNumPage : arr.length,
		itemPage : 5,
		showPageNum : 9,
		endPageNum : 1,
		currentPage : 0,
		linkTo : '',
		callback : function(num,obj){
			var maxElem = Math.min((num+1)*this.itemPage , this.allNumPage)
			$design.html('');
			for(var i=num*this.itemPage;i<maxElem;i++){
				$design.append(arr[i]);
			}
			$design.find('li:odd').attr('class', 'even');
			obj.find('.btn').on('click' , function(ev){
				ev.stopPropagation();
				$('body,html').animate({scrollTop:$design.offset().top},1000);
			});
			
			return false;
		}
	});
});